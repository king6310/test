/*
 * 形参与实参的顺序/个数的关系
 *
 * 实参的值，要赋给形参变量
 *
 */

function foo($x,$y,$z){
    return 2*$x + 3*$y + $z;
}

foo(1,2,3);

// 在调用的过程中，
// 实参,形参，从左到右严格的逐一对应
//一一对应上，然后实参值，挨个赋给形参

/*
 * 如果实参少于形参，没对应上
 */

//echo foo(1,2);  //warning警告

/*
 *  如果实参多于形参
 */

echo foo(1,2,3,4,5,6);  //还是11

//因为实参对号给形参赋值时，多出来的实参被舍弃掉
